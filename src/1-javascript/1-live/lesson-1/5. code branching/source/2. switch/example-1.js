const color = 'green'; // 'red', 'yellow', 'green'

switch (color) {
    case 'green':
        console.log('START');
        break;
    case 'red':
        console.log('STOP');
        break;
    case 'yellow':
        console.log('1 MIN');
        break;
    default:
        console.log("I don't know this color!");
}

